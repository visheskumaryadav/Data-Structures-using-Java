 package Arrays;

import java.util.List;

public class Util {

    public static void printArray(int[] arr){
        System.out.println();
        for(int a:arr){
            System.out.print(a+" ");
        }
    }

    public static void printArray(List<Integer> arr){
        System.out.println();
        for(int a:arr){
            System.out.print(a+" ");
        }
    }
    
}